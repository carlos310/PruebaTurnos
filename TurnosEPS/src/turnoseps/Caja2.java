    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        turnosEnVivo.setVisible(true);
        turnosEnVivo.getjTextField3().setText(turnosEnVivo.nombres.getFirst()+"|"+turnosEnVivo.cedulas.getFirst());
        for(int i=1;i<=turnosEnVivo.nombres.size();i++){
            int posicion= i-1;
            turnosEnVivo.nombres.add(posicion, turnosEnVivo.nombres.get(i));
        }
        for(int i=1;i<=turnosEnVivo.cedulas.size();i++){
            int posicion= i-1;
            turnosEnVivo.cedulas.add(posicion, turnosEnVivo.cedulas.get(i));
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

